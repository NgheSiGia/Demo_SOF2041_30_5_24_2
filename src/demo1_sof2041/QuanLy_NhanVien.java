    Model_NV readForm(){
        String maNv, ten, pass;
        boolean vaiTro;
        maNv = txt_MaNV.getText();
        ten = txt_Ten.getText();
        pass = txt_Mk.getText();
        if(rdo_TruongPhong.isSelected()){
            vaiTro = true;
        }else{
            vaiTro = false;
        }
        return new Model_NV(maNv, pass, ten, vaiTro);
    }
