    private void tbl_QLNVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_QLNVMouseClicked
        i = tbl_QLNV.getSelectedRow();
        txt_MaNV.setText(tbl_QLNV.getValueAt(i, 0).toString());
        txt_Mk.setText(tbl_QLNV.getValueAt(i, 1).toString());
        txt_Ten.setText(tbl_QLNV.getValueAt(i, 2).toString());
        if(tbl_QLNV.getValueAt(i, 3).toString().equalsIgnoreCase("Trưởng phòng")){
            rdo_TruongPhong.setSelected(true);
        }else{
            rdo_NhanVien.setSelected(true);
        }
    }//GEN-LAST:event_tbl_QLNVMouseClicked

    private void btn_XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_XoaActionPerformed
        if(this.readForm() != null){
            String maXoa = txt_MaNV.getText().toString();
            if(rp.xoa(maXoa) > 0){
                JOptionPane.showMessageDialog(this, "Xoa thanh cong");
                this.fillTable(rp.getAll());
            }else{
                JOptionPane.showMessageDialog(this, "Xoa that bai");
            }
        }
        txt_Ten.setText("");
        txt_MaNV.setText("");
        txt_Mk.setText("");
        txt_Mk2.setText("");
    }//GEN-LAST:event_btn_XoaActionPerformed

    private void btn_SuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SuaActionPerformed
        i = tbl_QLNV.getSelectedRow();
        if(i == -1){
            JOptionPane.showMessageDialog(this, "Ban chua chon dong");
        }else{
            String maSua = txt_MaNV.getText().toString();
            if(this.readForm() != null){
                if(rp.sua(this.readForm(), maSua) > 0){
                    JOptionPane.showMessageDialog(this, "Sua thanh cong");
                    this.fillTable(rp.getAll());
                }else{
                    JOptionPane.showMessageDialog(this, "Sua that bai");
                }
            }
        }
    }//GEN-LAST:event_btn_SuaActionPerformed

